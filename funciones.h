
/*Funcion que suma dos numeros enteros*/
int sumar(int a, int b)
{
	return a + b;
}

int restar(int c,int d)
{
	return c-d;
}

int multiplicar(int e,int f)
{
	if(e>0 && f>0)
	{
	   return e*f;
	}
	else
	{
		printf("\nNO SE PUEDE HACER LA MULTIPLICACION\n");
	}
	
}

float division(float g,float h)
{
	return g/h;
}
